<p class="titre"><a class="titre" name="pourquoi2">Pourquoi en faire partie ?</a></p>
<p>
Comme vous pouvez le voir dans les statuts, faire partie de l'association équivaut à faire partie de l'assemblée générale de l'association. Concrètement, cela veut dire participer donc aux assemblées générales, voter les décisions importantes, et qui sait faire partie du CA ou du bureau.<br>
De plus, les frais d'inscription (5 à l'année) permettent de financer en partie le jeu.
</p>

