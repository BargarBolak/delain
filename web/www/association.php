<p class="titre"><a class="titre" name="pourquoi">Pourquoi une association ?</a></p>
<p>
Le jeu des souterrains de Delain génère des frais. En effet, faire fonctionner un jeu comme celui-ci nécessite des dépenses (hébergement, nom de domaine, etc...), et donc des rentrées d'argent. Dans un premier temps, j'ai géré personnellement les rentrées d'argent, mais ceci comportait deux inconvénients majeurs :<br>
1 - C'est limite légal, car considéré comme un revenu, donc imposable, etc...<br>
2 - Cela peut refroidir certains joueurs de donner de l'argent à un particulier.<br>
Décision a donc été prise de créer une assocition à but non lucratif. Cette association a pour rôle de gérer le jeu. Cela veut dire gérer les entrées/sortie d'argent bien sur, mais aussi trouver peut être de nouveaux concepts (partenariats, etc...). Cette association est parue au JO du 19/06/2004.



</p>
